#include "../inc/pipex.h"

t_fds	*get_fds(void)
{
	static t_fds	*fds;

	if (!fds)
	{
		fds = ft_calloc(1, sizeof (t_fds));
		malloc_check(fds);
	}
	return (fds);
}
