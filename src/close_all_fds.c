#include "../inc/pipex.h"

void	close_all_fds(int fds[2][2])
{
	int	i;
	int	j;

	i = 0;
	while (i < 2)
	{
		j = 0;
		while (j < 2)
		{
			if (fds[i][j])
				close_(fds[i][j]);
			j++;
		}
		i++;
	}
}
